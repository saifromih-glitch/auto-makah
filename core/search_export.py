import httpx, json, os

# ═══ Web Search (DuckDuckGo — no API key) ═══

async def web_search(query: str, max_results: int = 5) -> dict:
    """Search the web using DuckDuckGo Instant Answer API."""
    try:
        async with httpx.AsyncClient(timeout=15) as c:
            r = await c.get("https://api.duckduckgo.com/", params={
                "q": query, "format": "json", "no_html": 1, "skip_disambig": 1,
            })
            data = r.json()
            results = []
            # Abstract
            if data.get("AbstractText"):
                results.append({"title": data.get("AbstractSource", "DuckDuckGo"), "snippet": data["AbstractText"], "url": data.get("AbstractURL", "")})
            # Related topics
            for topic in data.get("RelatedTopics", [])[:max_results]:
                if isinstance(topic, dict) and "Text" in topic:
                    results.append({"title": topic.get("FirstURL", "").split("/")[-1].replace("_"," "), "snippet": topic["Text"], "url": topic.get("FirstURL", "")})
            return {"query": query, "results": results, "total": len(results)}
    except Exception as e:
        return {"query": query, "error": str(e), "results": []}

# ═══ Wikipedia Summary ═══

async def wikipedia_summary(topic: str, lang: str = "ar") -> dict:
    """Get Wikipedia summary."""
    try:
        async with httpx.AsyncClient(timeout=15) as c:
            r = await c.get(f"https://{lang}.wikipedia.org/api/rest_v1/page/summary/{topic}", headers={"User-Agent": "AutoMakah/1.0"})
            if r.status_code == 200:
                data = r.json()
                return {
                    "title": data.get("title"),
                    "summary": data.get("extract", "")[:2000],
                    "url": data.get("content_urls", {}).get("desktop", {}).get("page", ""),
                    "image": data.get("thumbnail", {}).get("source", ""),
                }
            return {"title": topic, "error": f"Wikipedia returned {r.status_code}"}
    except Exception as e:
        return {"error": str(e)}

