# ═══ Text Stats ═══

def text_stats(text: str) -> dict:
    """Analyze text statistics."""
    words = text.split()
    chars = len(text)
    lines = text.count('\n') + 1
    arabic_chars = sum(1 for c in text if '\u0600' <= c <= '\u06ff')
    return {
        "words": len(words),
        "characters": chars,
        "characters_no_spaces": len(text.replace(' ','').replace('\n','')),
        "lines": lines,
        "arabic_characters": arabic_chars,
        "arabic_percentage": round(arabic_chars / chars * 100, 1) if chars > 0 else 0,
        "reading_time_seconds": round(len(words) / 3 * 60),  # 3 words/sec for Arabic
    }
